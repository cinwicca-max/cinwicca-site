// ── Apply translations ──
window.__lang = localStorage.getItem('sb_lang') || (navigator.language || 'en').slice(0, 2).toLowerCase();
if (!window.__i18n[window.__lang]) window.__lang = 'en';

function applyI18n(lang) {
  if (!window.__i18n[lang]) lang = 'en';
  window.__lang = lang;
  localStorage.setItem('sb_lang', lang);
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : (lang === 'es' ? 'es-ES' : 'en');

  const dict = window.__i18n[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] != null) el.innerHTML = dict[key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key] != null) el.setAttribute('placeholder', dict[key]);
  });

  document.querySelectorAll('.lang-switch button').forEach(b => {
    b.classList.toggle('active', b.getAttribute('data-lang') === lang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-switch button').forEach(b => {
    b.addEventListener('click', () => applyI18n(b.getAttribute('data-lang')));
  });
  applyI18n(window.__lang);
});
